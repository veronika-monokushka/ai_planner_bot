"""
Базовый класс для работы с JSON файлом

Этот класс предоставляет основу для всех репозиториев (хранилищ данных).
Он отвечает за:
1. Создание файла БД, если его нет
2. Загрузку и сохранение данных
3. Поддержание правильной структуры данных

Каждый репозиторий (пользователи, рецепты, планы и т.д.) наследует этот класс,
чтобы не дублировать код работы с файлами.
"""

import json
import os
from typing import Dict, Any, Optional


class BaseRepository:
    """
    Базовый класс для работы с JSON файлом.
    
    Отвечает за низкоуровневые операции с файлом:
    - создание файла при первом запуске
    - чтение данных из файла
    - запись данных в файл
    - поддержание целостности структуры данных
    
    Этот класс не знает о бизнес-логике (что такое пользователь, рецепт и т.д.).
    Он только работает с файлом как с хранилищем.
    
    Наследники (UserRepository, RecipeRepository и т.д.) добавляют
    бизнес-логику поверх этого базового класса.
    """
    
    def __init__(self, filename: str):
        """
        Инициализация репозитория.
        
        Args:
            filename: Путь к JSON файлу, где хранятся все данные
                      (например, "users_data.json")
        """
        self.filename = filename  # Сохраняем путь к файлу
        self._ensure_file_exists()  # Проверяем, существует ли файл
    
    def _ensure_file_exists(self):
        """
        Проверяет существование файла и создает его при необходимости.
        
        Отвечает за:
        - Создание пустого файла, если его нет
        - Инициализацию правильной структуры данных
        - Сохранение пустых словарей для всех типов данных
        
        Это защищенный метод (с _), его не нужно вызывать извне.
        Он вызывается автоматически при создании объекта.
        """
        if not os.path.exists(self.filename):
            # Файла нет - создаем с пустой структурой
            with open(self.filename, 'w', encoding='utf-8') as f:
                json.dump({
                    "users": {},          # Пользователи
                    "recipes": {},        # Рецепты
                    "meal_plans": {},     # Планы питания
                    "shopping_lists": {}, # Списки покупок
                    "reminders": {}       # Напоминания
                }, f, ensure_ascii=False, indent=2)
    
    def _load_data(self) -> Dict:
        """
        Загружает все данные из JSON файла.
        
        Отвечает за:
        - Чтение файла
        - Преобразование JSON в словарь Python
        - Обработку ошибок (если файл поврежден)
        - Гарантию, что возвращается правильная структура
        
        Returns:
            Dict: Словарь со всеми данными в формате:
                  {
                      "users": {...},
                      "recipes": {...},
                      "meal_plans": {...},
                      "shopping_lists": {...},
                      "reminders": {...}
                  }
        """
        try:
            # Пытаемся прочитать файл
            with open(self.filename, 'r', encoding='utf-8') as f:
                data = json.load(f)
                
                # Если файл пустой или содержит не словарь - возвращаем пустую структуру
                if not isinstance(data, dict):
                    return self._get_empty_data()
                
                # Убеждаемся, что все нужные ключи есть
                data = self._ensure_keys(data)
                return data
        except Exception:
            # При любой ошибке (файл не читается, битый JSON) возвращаем пустую структуру
            return self._get_empty_data()
    
    def _ensure_keys(self, data: Dict) -> Dict:
        """
        Гарантирует, что в данных присутствуют все необходимые ключи.
        
        Отвечает за:
        - Проверку наличия ключей для всех типов данных
        - Добавление отсутствующих ключей с пустыми словарями
        
        Args:
            data: Загруженные из файла данные
            
        Returns:
            Dict: Данные с гарантированно присутствующими ключами
        """
        required_keys = ['users', 'recipes', 'meal_plans', 'shopping_lists', 'reminders']
        for key in required_keys:
            if key not in data:
                data[key] = {}  # Добавляем отсутствующий ключ с пустым словарем
        return data
    
    def _get_empty_data(self) -> Dict:
        """
        Возвращает пустую структуру данных.
        
        Используется:
        - При создании нового файла
        - При ошибках чтения существующего файла
        - Как шаблон правильной структуры
        
        Returns:
            Dict: Словарь с пустыми словарями для всех типов данных
        """
        return {
            "users": {},
            "recipes": {},
            "meal_plans": {},
            "shopping_lists": {},
            "reminders": {}
        }
    
    def _save_data(self, data: Dict):
        """
        Сохраняет данные в JSON файл.
        
        Отвечает за:
        - Запись данных в файл
        - Форматирование JSON (отступы, кодировка)
        - Перезапись всего файла целиком
        
        Args:
            data: Словарь с данными для сохранения
        """
        with open(self.filename, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)